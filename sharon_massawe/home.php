    <div class="card">
      <img src="images/image6.jpg" class="card-img-top" alt="Hollywood Sign on The Hill"/>
      <div class="card-body">
        <h5 class="card-title">Our minerals</h5>
        <p class="card-text">
        Minerals are naturally occurring inorganic substances that have a specific chemical composition and crystalline structure. They are the building blocks of rocks and can be found in a wide variety of forms, ranging from metallic minerals like gold and copper to non-metallic minerals such as quartz and gypsum.
        Minerals are classified based on their composition and physical properties, including hardness, color, and luster. 
        </p>
      </div>
    </div>
  </div>
  <div class="col">
    <div class="card">
      <img src="images/image7.jpg" class="card-img-top" alt="Palm Springs Road"/>
      <div class="card-body">
        <h5 class="card-title">Asian Minerals</h5>
        <p class="card-text">
        Asia is home to a vast and diverse range of mineral resources, making it one of the world’s most significant regions for mineral extraction and production. The continent boasts abundant deposits of both metallic and non-metallic minerals that are crucial for various industries, including construction, energy, technology, and manufacturing. For example, China is the world’s largest producer of coal, rare earth elements, and also has significant reserves of aluminum, copper, and zinc.
         India is rich in iron ore, coal, and bauxite, which are vital for steel production and energy generation. 
        </p>
      </div>
    </div>
  </div>
  <div class="col">
    <div class="card">
      <img src="images/image8.jpg" class="card-img-top" alt="Los Angeles Skyscrapers"/>
      <div class="card-body">
        <h5 class="card-title">Geological Minerals</h5>
        <p class="card-text">
        Geological minerals found in caves are often remnants of geological processes that took place millions of years ago. These minerals have formed through a variety of natural processes, including the precipitation of dissolved minerals from water or the crystallization of elements deep within the Earth's crust. One of the most notable examples of ancient minerals found in caves is stalactites and stalagmites,
         which are primarily made of calcite (calcium carbonate). 
        </p>
      </div>
    </div>
  </div>
  <div class="col">
    <div class="card">
      <img src="images/image9.jpg" class="card-img-top" alt="Skyscrapers"/>
      <div class="card-body">
        <h5 class="card-title">Museum Preservation</h5>
        <p class="card-text">
        Minerals in museums are showcased for their scientific, aesthetic, and historical significance, providing a glimpse into Earth's geological processes. Common specimens include quartz in various forms like amethyst and citrine, prized for their striking colors and crystal formations, along with gold and diamonds, which are valued for their rarity and beauty. Fluorite, calcite, and pyrite are also popular due to their unique colors and crystal shapes, while copper-based minerals like malachite and azurite stand out for their vibrant hues. Meteorite samples, containing rare extraterrestrial minerals, offer a fascinating look into materials beyond Earth. 
      
        </p>
      </div>
    </div>
  </div>
  <div class="col">
    <div class="card">
      <img src="images/image12.jpg" class="card-img-top" alt="Skyscrapers"/>
      <div class="card-body">
        <h5 class="card-title">Learning Minerals</h5>
        <p class="card-text">
        
Learning about minerals is a fascinating way to explore the Earth's composition and understand the processes that shape our planet. Minerals are naturally occurring, inorganic solids with a specific chemical structure and physical properties. Key characteristics that help in identifying minerals include color, hardness (measured on the Mohs scale), crystal form, luster, and streak.
        </p>
      </div>
    </div>
  </div>
  <div class="col">
    <div class="card">
      <img src="images/image13.jpg" class="card-img-top" alt="Skyscrapers"/>
      <div class="card-body">
        <h5 class="card-title">Extraction of Minerals</h5>
        <p class="card-text">
        Mineral extraction is the process of obtaining valuable minerals from the Earth, typically through surface or underground mining methods. Surface mining, including open-pit, strip, and placer mining, is used for minerals near the surface, while underground mining, such as shaft and drift mining, is used for deeper deposits. Once minerals are extracted, they undergo processing like crushing, grinding, and flotation to separate valuable materials from waste. Afterward, minerals are refined for use in industries such as construction, electronics, and energy. 
 
        </p>
      </div>
